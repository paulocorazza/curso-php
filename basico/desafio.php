<div class="titulo">
    Desafio - Exibir o resultado da soma usando PHP
</div>


<p>Solucao 1 - usando tag PHP fora do HTML</p>
<ul>
    <li>1 + 1 = <?= 1 + 1 ?></li>
    <li>4 + 4 = <?= 4 + 4 ?></li>
    <li>8 + 8 = <?= 8 + 8 ?></li>
</ul>

<p>Solucao 2 - Usando tag PHP dentro do HTML</p><ul>
    <li>1 + 1 = <?php echo 1 + 1 ?></li>
    <li>4 + 4 = <?php echo 4 + 4 ?></li>
    <li>8 + 8 = <?php echo 8 + 8?></li>
</ul>